    fun translate(key: String, languageName: String): String {
        // Convert languageName (like "English", "বাংলা", "Hindi") to code
        val code = when (languageName.lowercase()) {
            "english" -> "en"
            "বাংলা" -> "bn"
            "bangla" -> "bn"
            "hindi" -> "hi"
            "arabic" -> "ar"
            "العربية" -> "ar"
            else -> "en" // default to English
        }
        val langMap = translations[code] ?: translations["en"]!!
        return langMap[key] ?: translations["en"]?.get(key) ?: key
    }
